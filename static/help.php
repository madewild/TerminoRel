<h3>Rechercher un terme dans les glossaires</h3>
<p>Tapez un mot ou une partie de mot pour retrouver toutes les entrées correspondantes.<br> 
La recherche est insensible à la casse et aux accents : "PEDA" retrouvera "assistant pédagogique" par exemple.</p>
<p>Par défaut, la langue source (sur laquelle s'effectue la recherche) est le français 
et la langue cible (pour laquelle des traductions sont recherchées) est l'anglais.<br>
Si vous voulez effectuer une recherche sur des termes en anglais, choisissez "anglais" comme langue source
et le français sera automatiquement sélectionné comme langue cible.</p>
<p>Tous les domaines sont également activés par défaut, mais vous avez la possibilité de restreindre la recherche
à un domaine en particulier (Titres et fonctions, Enseignement supérieur...).</p>

<h3>Afficher toutes les entrées d'un glossaire</h3>
<p>Si vous ne recherchez pas un terme en particulier, vous avez la possibilité d'afficher la liste de tous les termes d'un glossaire.<br>
Dans ce cas, vous aurez plusieurs centaines de résultats et vous pourrez naviguer alphabétiquement parmi ceux-ci.</p>

<h3>Télécharger un glossaire</h3>
<p>Enfin, les professionnels de la traduction peuvent télécharger des fichiers TBX 
qui sont lisibles par les outils de traduction assistée par ordinateur (TAO).</p>

